function adicao(a, b) {
  return a + b;
}

function subtracao(a, b) {
  return a - b;
}

function multiplicacao(a, b) {
  return a * b;
}

function divisao(a, b) {
  return a / b;
}

function modulo(a, b) {
  return a % b;
}

console.log(adicao(45, 15));
console.log(subtracao(45, 15));
console.log(multiplicacao(45, 15));
console.log(divisao(45, 15));
console.log(modulo(45, 15));