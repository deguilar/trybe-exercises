function soma(a, b) {
  return 'a + b = ' + (a + b);
}

function subtracao(a, b) {
  return 'a - b = ' + (a - b);
}

function multiplicacao(a, b) {
  return 'a * b = ' + (a * b);
}

function divisao(a, b) {
  return 'a / b = ' + (a / b);
}

function modulo(a, b) {
  return 'a % b = ' + (a % b);
}

console.log(soma(12, 6));
console.log(subtracao(12, 6));
console.log(multiplicacao(12, 6));
console.log(divisao(12, 6));
console.log(modulo(12, 6));
